def max_ran(maximum):

    lst = [random.randint(0, 9) for i in range(maximum)]
    string = ''.join(list(map(lambda x: str(x), lst)))

    way = os.path.join("data", "text.txt")

    with open(way, 'w', encoding='UTF-8') as file:
        file.write(str(string))

    with open(way, 'r', encoding='UTF-8') as file:
        string_new = list(file.read())

    string_new.extend([""])
    length = len(string_new)

    answer = []
    check = []
    for i in range(length - 1):
        if string_new[i] == string_new[i + 1]:
            check.append(string_new[i])
        elif string_new[i] == string_new[i - 1] and i > 0:
            check.append(string_new[i])
            if len(check) > len(answer):
                answer.clear()
                answer.extend(check)
                check.clear()
            else:
                check.clear()

    return string_new, answer


